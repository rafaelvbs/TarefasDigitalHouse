package br.digitalHouse.javaComObjeto.exercicio3;

public class atleta {
    private String nome;
    private int energia;
    private int nivel;

    public int getEnergia() {
        return energia;
    }

    public String getNome() {
        return nome;
    }

    public int getNivel() {
        return nivel;
    }

    public void setEnergia(int novaEnergia) {
        energia = novaEnergia;
    }

    public void setNome(String novoNome) {
        nome = novoNome;
    }

    public void setNivel(int novoNivel) {
        nivel = novoNivel;
    }

}
